// Logical Not (!)
// !(true) -> false
// !(false) -> true
 
 import java.util.*;

 public class Logical_Not {
	
	 public static void main(String args[]) {
		
		 int val1 = 5;
		 int val2 = 6;

		 System.out.println(!(val1 < val2)); // !(true) -> false

		 System.out.print(!(val1 > val2)); // !(false) -> true
	 }
 }
