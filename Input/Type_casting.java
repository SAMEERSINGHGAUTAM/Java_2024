// Type Casting

 import java.util.*;

 public class Type_casting {
	public static void main(String args[]) {
		Scanner sc = new Scanner(System.in);
		
		System.out.print("Enter a value : ");
		byte a = (byte) sc.nextDouble();
		System.out.print(a);

		System.out.print("Enter a number : ");
		char b = (char) sc.next();
		System.out.print(b);
	}

 }
