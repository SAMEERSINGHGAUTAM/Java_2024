// nextLong() - This method Scans the next token of the input as a long. This method returns the long scanned from the input. Range -> -9223372036854775808 to 9223372036854775807
 import java.util.*;

 public class Store_nextLong {
	public static void main(String args[]) {
		
		Scanner sc = new Scanner(System.in);

		System.out.print("Enter an integer : ");

		long value = sc.nextLong();

		System.out.print(value);
	}
 }
